# (30)How will you compare two lists?


# Define two lists
l1 = [1, 2, 3, 4]
l2 = [5, 6, 7, 8]

# Compare both lists 
if l1 == l2:
    print("Lists are equal")  # If both lists have the same elements in the same order, they are equal
else:
    print("Lists are not equal")  # If there is any difference, the lists are not equal
