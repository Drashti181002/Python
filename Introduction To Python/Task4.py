# (4) Write a Python program to check if a number is positive, negative or zero. 

n = int(input("Enter a Number : "))

if n > 0:
    print("Number is Positive!!")  # Print message if the number is positive

elif n == 0:
    print("Number is Zero!!")  # Print message if the number is zero

else:
    print("Number is Negative!!")  # Print message if the number is negative
