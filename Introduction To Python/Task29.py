# (29) Write a Python function to get the largest number, smallest num and sum of all from a list. 

# Define a list 
l = [1, 3, 5, 7, 9, 11, 13]

# Find the largest number in the list using max() function
large = max(l)

# Find the smallest number in the list using min() function
small = min(l)

# Calculate the sum of all elements in the list using sum() function
total = sum(l)  

# Print the results
print("Largest number is:", large)
print("Smallest number is:", small)
print("Sum of numbers is:", total)
